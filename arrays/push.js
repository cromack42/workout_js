/* O push() é um método onde adiciona se um ou mais elementos no fim de uma array 
e retorna o novo comprimento da mesma */

let animais = ['porco', 'cabra', 'ovelha']

const conta = animais.push('vaca')
console.log('Matriz (Array) adicionada: ', conta, animais)

animais.push('galinha', 'gato', 'cachorro')
console.log(animais)

/* O método push() adiciona valor para uma array. */

